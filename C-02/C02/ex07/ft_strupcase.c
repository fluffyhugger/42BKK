char	*ft_strupcase(char *str)
{
	int	num;

	num = 0;
	while (str[num] != '\0')
	{
		if (str[num] >= 'a' && str[num] <= 'z')
		{
			str[num] -= 32;
		}
		num++;
	}
	return (str);
}
